interface Doodle {
    path: string;
    top: number;
    left: number;
    size: number;
    opacity: number;
    rotation: number;
    duration: number;
    delay: number;
}

/** Layout fixo e bem distribuído para uso dentro de cards */
const CARD_LAYOUT: Array<{ top: number; left: number; size: number; rotation: number; pathIndex: number }> = [
    { top: 6, left: 4, size: 90, rotation: -20, pathIndex: 0 },
    { top: 60, left: 2, size: 120, rotation: 15, pathIndex: 2 },
    { top: 70, left: 30, size: 80, rotation: -30, pathIndex: 3 },
    { top: 12, left: 55, size: 70, rotation: 25, pathIndex: 4 },
    { top: 50, left: 70, size: 100, rotation: -15, pathIndex: 1 },
    { top: 4, left: 86, size: 85, rotation: 20, pathIndex: 3 },
];

interface ScribbleBackgroundProps {
    /** Quantidade de rabiscos (ignorado em variant="card") */
    count?: number;
    /** "sides": laterais (fundo geral). "spread": aleatório. "card": layout fixo distribuído */
    variant?: "sides" | "spread" | "card";
    /** Cor base dos rabiscos */
    color?: string;
    /** className extra para o container */
    className?: string;
}

const rand = (min: number, max: number) => Math.random() * (max - min) + min;

const ScribbleBackground = ({
    count = 14,
    variant = "sides",
    color = "#4B4B4B",
    className = "",
}: ScribbleBackgroundProps) => {
    const doodles = useMemo<Doodle[]>(() => {
        // Layout fixo para cards
        if (variant === "card") {
            return CARD_LAYOUT.map((d, i) => ({
                path: SCRIBBLE_PATHS[d.pathIndex],
                top: d.top,
                left: d.left,
                size: d.size,
                opacity: 0.14,
                rotation: d.rotation,
                duration: 9 + i * 1.5,
                delay: i * 0.8,
            }));
        }

        return Array.from({ length: count }).map((_, i) => {
            // Nas laterais: left fica entre 0-12% ou 88-100%
            let left: number;
            if (variant === "sides") {
                left = i % 2 === 0 ? rand(0, 12) : rand(86, 99);
            } else {
                left = rand(2, 92);
            }

            return {
                path: SCRIBBLE_PATHS[Math.floor(rand(0, SCRIBBLE_PATHS.length))],
                top: rand(2, 94),
                left,
                size: rand(90, 240),
                opacity: rand(0.05, 0.2),
                rotation: rand(-40, 40),
                duration: rand(7, 16),
                delay: rand(0, 6),
            };
        });
    }, [count, variant]);

    return (
        <div
            aria-hidden="true"
            className={`pointer-events-none absolute inset-0 overflow-hidden ${className}`}
        >
            {doodles.map((d, i) => (
                <svg
                    key={i}
                    viewBox="0 0 200 200"
                    fill="none"
                    xmlns="http://www.w3.org/2000/svg"
                    className="absolute scribble-float"
                    style={{
                        top: `${d.top}%`,
                        left: `${d.left}%`,
                        width: `${d.size}px`,
                        height: `${d.size}px`,
                        opacity: d.opacity,
                        ["--rot" as string]: `${d.rotation}deg`,
                        animationDuration: `${d.duration}s`,
                        animationDelay: `${d.delay}s`,
                    }}
                >
                    <path
                        d={d.path}
                        stroke={color}
                        strokeWidth={4}
                        strokeLinecap="round"
                        strokeLinejoin="round"
                        fill="none"
                    />
                </svg>
            ))}
        </div>
    );
};

export default ScribbleBackground;
